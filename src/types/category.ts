// src/types/category.ts
export interface Category {
  id: string; // Уникальный идентификатор категории (UUID)
  budget_id: string; // ID бюджета, к которому относится категория (UUID)
  name: string; // Название категории
  limit_amount: number; // Выделенная сумма (лимит) на эту категорию
  created_at: string; // Дата создания (строка ISO)
  updated_at: string; // Дата обновления (строка ISO)
  spent: number; // Сумма расходов по категории (рассчитывается на бэкенде)
  income: number; // Сумма доходов по категории (рассчитывается на бэкенде)
  balance: number; // Текущий баланс (лимит - расходы + доходы) (рассчитывается на бэкенде)
  progress: number; // Процент выполнения (рассчитывается на бэкенде, 0-100 или другое представление)
  transaction_count: number; // Количество транзакций (рассчитывается на бэкенде)
}
